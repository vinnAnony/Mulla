package com.vinnjeru.roomdb2.Biom;

import android.Manifest;
import android.app.Activity;
import android.content.Context;
import android.content.Intent;
import android.content.pm.PackageManager;
import android.hardware.fingerprint.FingerprintManager;
import android.os.Build;
import android.os.CancellationSignal;
import android.support.annotation.RequiresApi;
import android.support.v4.app.ActivityCompat;
import android.widget.Toast;

import com.vinnjeru.roomdb2.Authent;
import com.vinnjeru.roomdb2.MainActivity;

@RequiresApi(api = Build.VERSION_CODES.M)
public class Helper extends FingerprintManager.AuthenticationCallback {

    private Context context;
    public Helper(Context mContext) {
        context = mContext;
    }

    public void startAuth(FingerprintManager manager, FingerprintManager.CryptoObject cryptoObject) {
        CancellationSignal cancellationSignal = new CancellationSignal();
        if (ActivityCompat.checkSelfPermission(context, 	Manifest.permission.USE_FINGERPRINT) != PackageManager.PERMISSION_GRANTED) {
            return;
        }
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.M) {
            manager.authenticate(cryptoObject, cancellationSignal, 0, this, null);
        }
    }

    @Override
    public void onAuthenticationError(int errMsgId, CharSequence errString) {
        this.update("Fingerprint Authentication error\n" + errString);
    }

    @Override
    public void onAuthenticationHelp(int helpMsgId, CharSequence helpString) {
        this.update("Fingerprint Authentication help\n" + helpString);
    }

    @Override
    public void onAuthenticationFailed() {
        this.update("Fingerprint Authentication failed.");
    }

    @Override
    public void onAuthenticationSucceeded(FingerprintManager.AuthenticationResult result) {
        Authent.alert.hide();
        ((Activity) context).finish();
        Intent inty = new Intent(context,MainActivity.class);
        context.startActivity(inty);
        Toast.makeText(context, "Authentication succeeded", Toast.LENGTH_SHORT).show();

    }

    private void update(String e){
        Toast.makeText(context, e, Toast.LENGTH_SHORT).show();
    }

}